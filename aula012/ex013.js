var agora = new Date()
var diasem = agora.getDay()
console.log(diasem)
switch(diasem){
    case 0:
        console.log('Domingo')
        break
    case 1:
        console.log('Segunda')
        break
    case 2:
        console.log('Terça')
        break
    case 3:
        console.log('quarta')
        break

    case 4:
        console.log('Quinta')
        break
    case 5:
        console.log('Graças a Deus é sexta feira heim')
        break

    case 6:
        console.log('Sábado')
        break

    default:
        console.log('[ERRO] dia inválido')
    }
    

    








